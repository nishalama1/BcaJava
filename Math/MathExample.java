package Math;

public class MathExample {
public static void main(String[] args) {
    int a =2;
    int b=2;
    int sum=a+b;
    System.out.println(java.lang.Math.abs(sum));

    double degree=0.67;
    System.out.println(java.lang.Math.acos(degree));

    int sqr=2;
    System.out.println(java.lang.Math.sqrt(sqr));

    int cbrt=2;
    System.out.println(java.lang.Math.cbrt(cbrt));

    double floor=2.10;
    System.out.println(java.lang.Math.floor(floor));
    

    double log=2;
    System.out.println(java.lang.Math.log(log));

    double ceil=2.10;
    System.out.println(java.lang.Math.ceil(ceil));

    double atan=2;
    System.out.println(java.lang.Math.atan(atan));
    
    
    
} 
}